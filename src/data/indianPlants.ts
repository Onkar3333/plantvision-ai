// Helper functions
export const getPlantById = (id: number): PlantData | undefined => {
  return indianPlants.find(plant => plant.id === id);
};

export const getPlantsByCategory = (category: string): PlantData[] => {
  if (category === "all") return indianPlants;
  return indianPlants.filter(plant => 
    plant.category.toLowerCase() === category.toLowerCase() ||
    plant.type === category.toLowerCase() ||
    plant.season.toLowerCase().includes(category.toLowerCase())
  );
};

export const searchPlants = (query: string): PlantData[] => {
  const lowerQuery = query.toLowerCase();
  return indianPlants.filter(plant =>
    plant.name.toLowerCase().includes(lowerQuery) ||
    plant.hindiName?.includes(query) ||
    plant.scientificName.toLowerCase().includes(lowerQuery) ||
    plant.category.toLowerCase().includes(lowerQuery) ||
    plant.type.toLowerCase().includes(lowerQuery)
  );
};

// Format price in Indian Rupees
export const formatPrice = (price: number): string => {
  return `₹${price.toLocaleString('en-IN')}`;
};
